class Student{
    private String name;
    private int age;
    private String distric;

    public void setName(String name){
        this.name = name;
    }
    public void setAge(int b){
        this.age = b;
    }
    public void setDistric(String distric){
        this.distric = distric;
    }
    public String getName(){
        return this.name;
    }
    public int getAge(){
        return this.age;
    }
    public String getDistric(){
        return this.distric;
    }
}